function sortWithIndeces(toSort)
{
  for (var i = 0; i < toSort.length; i++) {    toSort[i] = [toSort[i], i];  }
  toSort.sort(function(left, right)
    {
    var l=parseFloat(left);
    var r=parseFloat(right);
    if(l<r) return -1;
    else    return  1;
    //zeee.sort(function(a, b) {  return a - b;});
    //return left-right;
    //return parseFloat(left[0]) < parseFloat(right[0]) ? -1 : 1;
    });
  toSort.sortIndices = [];
  for (var j = 0; j < toSort.length; j++)
  {
    toSort.sortIndices.push(toSort[j][1]);
    toSort[j] = toSort[j][0];
  }
  return toSort;
}






